package com.picsaxis.prioritystatus.model;

public class OfferPriorityModel 
{
	private int id;
	private int offerId;
	private String offerName;
	private int offerPriority;
	private String advertiserName;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getOfferId() {
		return offerId;
	}
	public void setOfferId(int offerId) {
		this.offerId = offerId;
	}
	public String getOfferName() {
		return offerName;
	}
	public void setOfferName(String offerName) {
		this.offerName = offerName;
	}
	public int getOfferPriority() {
		return offerPriority;
	}
	public void setOfferPriority(int offerPriority) {
		this.offerPriority = offerPriority;
	}
	public String getAdvertiserName() {
		return advertiserName;
	}
	public void setAdvertiserName(String advertiserName) {
		this.advertiserName = advertiserName;
	}
	
	
	
	

}
